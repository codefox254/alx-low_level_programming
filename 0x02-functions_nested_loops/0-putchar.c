#include "main.h"

/**
 * this program printes _putchar
 * followed by a new line
 * should return 0 on sucess
 */

int _putchar(char c) {
    return putchar(c);
}

int main(void) {
    
    _putchar('_');
    _putchar('p');
    _putchar('u');
    _putchar('t');
    _putchar('c');
    _putchar('h');
    _putchar('a');
    _putchar('r');
    _putchar('\n');
    
    return 0;
}
